import array

def merge_sort(arr):
    if len(arr) > 1:
        mid = len(arr) // 2
        left_half = arr[:mid]
        right_half = arr[mid:]

        merge_sort(left_half)
        merge_sort(right_half)

        i = j = k = 0

        while i < len(left_half) and j < len(right_half):
            if left_half[i] < right_half[j]:
                arr[k] = left_half[i]
                i += 1
            else:
                arr[k] = right_half[j]
                j += 1
            k += 1

        while i < len(left_half):
            arr[k] = left_half[i]
            i += 1
            k += 1

        while j < len(right_half):
            arr[k] = right_half[j]
            j += 1
            k += 1

# Dynamic input
n = int(input("Enter the number of elements: "))
arr = array.array('i', [0] * n)

print("Enter the elements:")
for i in range(n):
    arr[i] = int(input())

merge_sort(arr)

print("Sorted array:")
for i in range(n):
    print(arr[i], end=" ")
